package com.alex.oop.calculator.impl;

/**
 * 4 Methods for every function - sum , subtraction, multiplication, division
 * @param - L L , D D , L D , D L(where D = Double and L = Long)
 *
 */
public class Calculator 
{

	public static Long sum(Long num1 , Long num2)
	{
		return num1 + num2;
	}
	
	public static Double sum(Double num1 , Double num2)
	{
		return num1 + num2;
	}
	
	public static Double sum(Long num1 , Double num2)
	{
		return num1 + num2;
	}
	
	public static Double sum(Double num1 , Long num2)
	{
		return num1 + num2;
	}
	
	public static Long subtraction(Long num1 , Long num2)
	{
		return num1 - num2;
	}
	
	public static Double subtraction(Double num1 , Double num2)
	{
		return num1 - num2;
	}	
	
	public static Double subtraction(Long num1 , Double num2)
	{
		return num1 - num2;
	}
	
	public static Double subtraction(Double num1 , Long num2)
	{
		return num1 - num2;
	}

	public static Long multiplication(Long num1 , Long num2)
	{
		return num1 * num2;
	}
	
	public static Double multiplication(Double num1 , Double num2)
	{
		return num1 * num2;
	}
	
	public static Double multiplication(Long num1 , Double num2)
	{
		return num1 * num2;
	}
	
	public static Double multiplication(Double num1 , Long num2)
	{
		return num1 * num2;
	}
	
	public static Double division(Long num1 , Long num2)
	{
		return num1.doubleValue() / num2 ;
	} 
	
	public static Double division(Double num1 , Double num2)
	{
		return num1 / num2;
	}
	
	public static Double division(Long num1 , Double num2)
	{
		return num1 / num2;
	}
	
	public static Double division(Double num1 , Long num2)
	{
		return num1 / num2;
	}
}
